def get_token() -> str:
    """Get Azure AD token for Cognitive Services."""
    result = subprocess.run(
        [
            "az", "account", "get-access-token",
            "--resource", "https://cognitiveservices.azure.com",
            "--query", "accessToken",
            "-o", "tsv",
        ],
        capture_output=True,
        text=True,
    )
    if result.returncode != 0:
        raise RuntimeError(f"Failed to get token: {result.stderr}")
    return result.stdout.strip()


def create_agent(token: str, agent_def: dict) -> dict:
    """Create an agent via the OpenAI Assistants API on Azure."""
    import urllib.request

    url = f"{ENDPOINT}openai/assistants?api-version={API_VERSION}"

    payload = {
        "model": agent_def["model"],
        "name": agent_def["name"],
        "description": f"AIUC-1 SOC 2 Compliance Lab — {agent_def['display_name']}",
        "instructions": agent_def["instructions"],
        "tools": agent_def["tools"],
        "metadata": agent_def["metadata"],
        "temperature": 0.1,  # Low temperature for deterministic compliance outputs
        "top_p": 0.9,
    }

    data = json.dumps(payload).encode("utf-8")
    req = urllib.request.Request(
        url,
        data=data,
        headers={
            "Authorization": f"Bearer {token}",
            "Content-Type": "application/json",
        },
        method="POST",
    )

    try:
        with urllib.request.urlopen(req, timeout=60) as resp:
            return json.loads(resp.read().decode("utf-8"))
    except urllib.error.HTTPError as e:
        error_body = e.read().decode("utf-8")
        raise RuntimeError(
            f"Failed to create agent '{agent_def['name']}': "
            f"HTTP {e.code} — {error_body}"
        )


def list_existing_agents(token: str) -> list:
    """List all existing agents."""
    import urllib.request

    url = f"{ENDPOINT}openai/assistants?api-version={API_VERSION}&limit=100"
    req = urllib.request.Request(
        url,
        headers={"Authorization": f"Bearer {token}"},
        method="GET",
    )

    try:
        with urllib.request.urlopen(req, timeout=30) as resp:
            data = json.loads(resp.read().decode("utf-8"))
            return data.get("data", [])
    except urllib.error.HTTPError as e:
        error_body = e.read().decode("utf-8")
        raise RuntimeError(f"Failed to list agents: HTTP {e.code} — {error_body}")


def delete_agent(token: str, agent_id: str) -> None:
    """Delete an existing agent."""
    import urllib.request

    url = f"{ENDPOINT}openai/assistants/{agent_id}?api-version={API_VERSION}"
    req = urllib.request.Request(
        url,
        headers={"Authorization": f"Bearer {token}"},
        method="DELETE",
    )

    try:
        with urllib.request.urlopen(req, timeout=30) as resp:
            resp.read()
    except urllib.error.HTTPError as e:
        error_body = e.read().decode("utf-8")
        print(f"  Warning: Failed to delete agent {agent_id}: {error_body}")


