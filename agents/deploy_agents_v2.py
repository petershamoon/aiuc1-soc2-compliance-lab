def main():
    """Main deployment function."""
    print("=" * 70)
    print("AIUC-1 SOC 2 Compliance Lab — Agent Deployment (v2 SDK)")
    print("=" * 70)
    print(f"Project Endpoint: {PROJECT_ENDPOINT}")
    print()

    try:
        print("[1/4] Authenticating and connecting to Azure AI Project...")
        credential = DefaultAzureCredential()
        project_client = AIProjectClient(endpoint=PROJECT_ENDPOINT, credential=credential)
        print("  ✓ Connected successfully.")

        # Clean up existing agents
        print("\n[2/4] Checking for and deleting existing agent versions...")
        our_names = {a["name"] for a in AGENTS}
        for agent_name in our_names:
            try:
                # The new SDK uses versions. We delete all versions to start clean.
                existing_versions = project_client.agents.list_versions(name=agent_name)
                for v in existing_versions:
                    print(f"  Deleting existing agent: {v.name} (version: {v.version})")
                    project_client.agents.delete_version(name=v.name, version=v.version)
            except ResourceNotFoundError:
                print(f"  No existing agent named 	'{agent_name}\' found. Skipping.")
        print("  ✓ Cleanup complete.")

        # Deploy agents
        print("\n[3/4] Deploying new agents...")
        results = {}
        for i, agent_def in enumerate(AGENTS, 1):
            print(f"\n  [{i}/{len(AGENTS)}] {agent_def['display_name']} ({agent_def['model']})...")
            
            agent_definition = PromptAgentDefinition(
                model=agent_def["model"],
                instructions=agent_def["instructions"],
                tools=agent_def["tools"],
                # metadata is not a direct parameter in PromptAgentDefinition
                # It will be part of the agent version resource
            )

            created_agent = project_client.agents.create_version(
                agent_name=agent_def["name"],
                definition=agent_definition,
                display_name=agent_def["display_name"],
                description=f"AIUC-1 SOC 2 Compliance Lab — {agent_def['display_name']}",
                tags=agent_def["metadata"] # Use tags for metadata
            )

            results[agent_def["name"]] = {
                "id": created_agent.id,
                "name": created_agent.name,
                "version": created_agent.version,
                "display_name": created_agent.display_name,
                "model": agent_def["model"],
                "tools_count": len(agent_def["tools"]),
                "tool_names": [t.name for t in agent_def["tools"]],
            }
            print(f"        ✓ Created: {created_agent.name} (version: {created_agent.version})")

        # Summary
        print("\n" + "=" * 70)
        print("[4/4] Deployment Summary")
        print("=" * 70)
        for name, info in results.items():
            print(f"  {info['display_name']:25s} | {info['name']:20s} | v{info['version']:<5} | {info['tools_count']} tools")
        
        print("\nDeployment complete. Agents are now available in the Foundry Studio.")
        print("=" * 70)

        # Save config
        config_path = os.path.join(os.path.dirname(__file__), "agent_config_v2.json")
        with open(config_path, "w") as f:
            json.dump({"agents": results}, f, indent=2)
        print(f"\nAgent configuration saved to: {config_path}")

    except Exception as e:
        print(f"\n✗ Deployment failed: {e}", file=sys.stderr)
        sys.exit(1)

if __name__ == "__main__":
    main()
